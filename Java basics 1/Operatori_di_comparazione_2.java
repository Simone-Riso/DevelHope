public class Operatori_di_comparazione_2 {
    public static void main(String[] args) {
        char carattere1 = 'x';
        char carattere2 = 'y';
        System.out.println("Il primo carattere è: " + carattere1);
        System.out.println("Il secondo carattere è: " + carattere2);

    }

    public static boolean areEquals(char carattere1, char carattere2) {
        return carattere1 == carattere2;
    }
}
